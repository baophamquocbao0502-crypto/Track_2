# Bước 5: Tạo Generator và Model

# ✅ Batch Generator - không load hết RAM, augment on-the-fly
def batch_generator(image_paths, steerings, batch_size=100, is_training=True):
    while True:
        batch_img = []
        batch_st  = []
        idx = np.random.permutation(len(image_paths))
        for i in range(0, len(image_paths) - batch_size + 1, batch_size):
            batch_idx = idx[i:i + batch_size]
            for j in batch_idx:
                img = mpimg.imread(image_paths[j])
                st  = steerings[j]
                if is_training:
                    img, st = augment_image(img, st)
                batch_img.append(img_preprocess(img))
                batch_st.append(st)
            yield (np.array(batch_img), np.array(batch_st))
            batch_img, batch_st = [], []

# ✅ Model cải tiến - Dropout giảm xuống 0.2-0.3
def nvidia_model():
    model = Sequential([
        Conv2D(24, (5,5), strides=(2,2), input_shape=(66,200,3), activation='elu'),
        Conv2D(36, (5,5), strides=(2,2), activation='elu'),
        Conv2D(48, (5,5), strides=(2,2), activation='elu'),
        Conv2D(64, (3,3), activation='elu'),
        Conv2D(64, (3,3), activation='elu'),
        Dropout(0.3),
        Flatten(),
        Dense(100, activation='elu'),
        Dropout(0.2),
        Dense(50, activation='elu'),
        Dropout(0.2),
        Dense(10, activation='elu'),
        Dense(1)
    ])
    model.compile(Adam(learning_rate=0.001), loss='mse')
    return model

model = nvidia_model()
model.summary()

