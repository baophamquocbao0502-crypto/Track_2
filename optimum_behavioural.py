def augment_image(img, steering):
     # 1. Zoom
    if np.random.rand() < 0.5:
        scale = np.random.uniform(1.0, 1.3)
        h, w = img.shape[:2]
        M = cv2.getRotationMatrix2D((w/2, h/2), 0, scale)
        img = cv2.warpAffine(img, M, (w, h))
    # Flip ngang
    if np.random.rand() < 0.5:
        img = cv2.flip(img, 1)
        steering = -steering
    # Dịch chuyển ngang
    if np.random.rand() < 0.5:
        tx = np.random.randint(-50, 50)
        steering += tx * 0.002
        M = np.float32([[1, 0, tx], [0, 1, 0]])
        img = cv2.warpAffine(img, M, (img.shape[1], img.shape[0]))
    # Thay đổi độ sáng
    if np.random.rand() < 0.5:
        img = cv2.cvtColor(img, cv2.COLOR_RGB2HSV).astype(float)
        img[:, :, 2] = np.clip(img[:, :, 2] * np.random.uniform(0.4, 1.2), 0, 255)
        img = cv2.cvtColor(img.astype(np.uint8), cv2.COLOR_HSV2RGB)
    # Shadow ngẫu nhiên
    if np.random.rand() < 0.5:
        h, w = img.shape[:2]
        x1, x2 = np.random.randint(0, w, 2)
        mask = np.zeros_like(img)
        pts = np.array([[x1, 0], [x2, h], [0, h], [0, 0]])
        cv2.fillPoly(mask, [pts], (60, 60, 60))
        img = cv2.addWeighted(img, 1, mask, 0.4, 0)
    return img, steering

# ✅ Preprocessing
def img_preprocess(img):
    img = img[60:135, :, :]
    img = cv2.cvtColor(img, cv2.COLOR_RGB2YUV)
    img = cv2.GaussianBlur(img, (3, 3), 0)
    img = cv2.resize(img, (200, 66))
    img = img / 255.0
    return img

