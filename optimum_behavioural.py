    batch_generator(X_train, y_train, batch_size, is_training=True),
    steps_per_epoch=steps_per_epoch,
    epochs=50,
    validation_data=batch_generator(X_valid, y_valid, batch_size, is_training=False),
    validation_steps=validation_steps,
    callbacks=callbacks,
    verbose=1
